package com.fitcoach.infrastructure.ai.router;

/**
 * Типы AI запросов для маршрутизации между различными провайдерами
 * 
 * FOOD_ANALYSIS - анализ фотографий еды (роутится в DeepSeek R1 для экономии)
 * NUTRITION_ADVICE - персонализированные советы по питанию (роутится в Gemini Flash)
 * COMPLEX_QUERY - сложные запросы требующие reasoning (остаются в GPT-4)
 * CHAT_RESPONSE - обычные чат ответы (роутится в более дешевые модели)
 */
public enum AIRequestType {
    
    /**
     * Анализ фотографий еды - определение продуктов и примерных порций
     * Использует: DeepSeek R1 (~$0.14/1M токенов)
     * Экономия: 95% по сравнению с GPT-4V
     */
    FOOD_ANALYSIS,
    
    /**
     * Персонализированные советы по питанию на основе профиля пользователя
     * Использует: Gemini 2.5 Flash (~$0.075/1M токенов)
     * Экономия: 97% по сравнению с GPT-4
     */
    NUTRITION_ADVICE,
    
    /**
     * Сложные запросы требующие глубокого reasoning и анализа
     * Использует: GPT-4 (оригинальная цена, но только для критичных случаев)
     */
    COMPLEX_QUERY,
    
    /**
     * Обычные чат ответы тренера
     * Использует: Gemini 2.5 Flash для базовых диалогов
     * Экономия: 97% по сравнению с GPT-4
     */
    CHAT_RESPONSE,
    
    /**
     * Генерация планов тренировок
     * Использует: Gemini 2.5 Flash
     * Экономия: 97% по сравнению с GPT-4
     */
    WORKOUT_PLANNING,
    
    /**
     * Анализ прогресса пользователя
     * Использует: DeepSeek R1 для математических расчетов
     */
    PROGRESS_ANALYSIS
} 